const divFlex = document.querySelector(".d-flex");
const modalContainer = document.querySelector(".modal-container");
const modal = document.querySelector(".modal");

function renderProduct(productItems) {
    divFlex.innerHTML = "";
    const aside = document.createElement("aside");
    const main = document.createElement("main");
    const card = document.createElement("div");
    card.classList.add("card");

    aside.classList.add("side-bar");

    divFlex.append(main);
    divFlex.append(aside);
    divFlex.append(card);


    productItems.forEach(product => {
        const ul = document.createElement("ul");
        const li = document.createElement("li");
        const img = document.createElement("img");


        li.innerText = product.name;
        img.src = product.img;
        img.alt = product.name;
        li.append(img);
        ul.append(li);
        aside.append(ul);

        li.addEventListener("click", () => {
                main.innerHTML = "";
                product.products.forEach(item => {
                const div = document.createElement("div");
                const img = document.createElement("img");
                const h3 = document.createElement("h3");
                img.src = item.img;
                img.alt = item.name;
                h3.innerText = item.name;
                div.append(img);
                div.append(h3);
                main.append(div);


        div.addEventListener("click", () => {
                card.innerHTML = "";

                const cardContainer = document.createElement("div");
                const img = document.createElement("img");
                const name = document.createElement("h3");
                const description = document.createElement("p");
                const price = document.createElement("button");
                cardContainer.classList.add("card-container");

                img.src = item.img;
                img.alt = item.name;
                name.innerText = item.name;
                description.innerText = item.description;
                price.innerText = item.price;

                cardContainer.append(img);
                cardContainer.append(name);
                cardContainer.append(description);
                cardContainer.append(price);

                card.append(cardContainer);


                price.addEventListener("click", () => {
                    const p = document.createElement("p");
                    p.innerText = "Товар куплено! Дякуємо за ваш вибір!";
                    modal.append(p);
                    modalContainer.classList.remove("d-none");
                    main.classList.add("d-none");
                    card.classList.add("d-none");

                    function deleteModal() {
                         window.setTimeout(deletModalContainer, 2000);
                    }

                    function deletModalContainer() {
                        modalContainer.classList.add("d-none");
                        p.remove();
                        renderProduct(productItems);
                    }
                    deleteModal();

                })

            })
            })

        })
    })
}

renderProduct(productItems);





